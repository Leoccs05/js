/*  Operadores de Comparação
>     maior que
>=    maior que ou igual que
<     menor que
<=    menor que ou igual que
==    igualdade                 (valor) //Não é recomendavel usar esse
===   igualdade estrita         (valor e tipo)
!=    diferente                 (valor) //Não é recomendavel usar esse
!==   diferente estrito         (valor e tipo)
*/
let comp = 10 > 5;
console.log(comp);

comp = 10 >= 10;
console.log(comp);

console.log("-----------")

comp = 10 < 11;
console.log(comp);

comp = 10 <= 10;
console.log(comp);

console.log("-----------")

comp = 10 == '10';
console.log(comp);

comp = 10 === '10';
console.log(comp);

console.log("-----------")

comp = 10 != '10';
console.log(comp);

comp = 10 !== '10';
console.log(comp);

